import { describe, it, expect, vi, beforeEach } from 'vitest';
import { onMountedOrActivated } from '/@/hooks/core/onMountedOrActivated';

// Mock Vue composition functions
vi.mock('vue', () => ({
  nextTick: vi.fn((fn) => Promise.resolve().then(fn)),
  onMounted: vi.fn(),
  onActivated: vi.fn(),
}));

// Import mocked functions
import { nextTick, onMounted, onActivated } from 'vue';

// Build configuration mocks
Object.defineProperty(globalThis, '__COLOR_PLUGIN_OUTPUT_FILE_NAME__', {
  value: 'mock-theme.css', writable: true
});

describe('hooks/core/onMountedOrActivated', () => {
  let hookFn: any;
  let mountedCallback: any;
  let activatedCallback: any;

  beforeEach(() => {
    vi.clearAllMocks();
    hookFn = vi.fn();

    // Capture the callbacks passed to onMounted and onActivated
    (onMounted as any).mockImplementation((callback: any) => {
      mountedCallback = callback;
    });

    (onActivated as any).mockImplementation((callback: any) => {
      activatedCallback = callback;
    });
  });

  it('should register onMounted and onActivated hooks', () => {
    onMountedOrActivated(hookFn);

    expect(onMounted).toHaveBeenCalledTimes(1);
    expect(onActivated).toHaveBeenCalledTimes(1);
    expect(typeof mountedCallback).toBe('function');
    expect(typeof activatedCallback).toBe('function');
  });

  it('should call hook function on mounted', async () => {
    onMountedOrActivated(hookFn);

    // Simulate mounted lifecycle
    mountedCallback();

    expect(hookFn).toHaveBeenCalledTimes(1);
    expect(nextTick).toHaveBeenCalledTimes(1);
  });

  it('should call hook function on activated after mounted', async () => {
    onMountedOrActivated(hookFn);

    // Simulate mounted lifecycle first
    mountedCallback();

    // Wait for nextTick to set mounted flag
    await (nextTick as any).mock.calls[0][0]();

    // Clear previous calls
    hookFn.mockClear();

    // Simulate activated lifecycle
    activatedCallback();

    expect(hookFn).toHaveBeenCalledTimes(1);
  });

  it('should not call hook function on activated before mounted', () => {
    onMountedOrActivated(hookFn);

    // Simulate activated without mounted first
    activatedCallback();

    expect(hookFn).not.toHaveBeenCalled();
  });

  it('should handle multiple activations after mounted', async () => {
    onMountedOrActivated(hookFn);

    // Simulate mounted lifecycle
    mountedCallback();

    // Wait for nextTick to set mounted flag
    await (nextTick as any).mock.calls[0][0]();

    // Clear previous calls
    hookFn.mockClear();

    // Simulate multiple activations
    activatedCallback();
    activatedCallback();
    activatedCallback();

    expect(hookFn).toHaveBeenCalledTimes(3);
  });

  it('should work with different hook functions', () => {
    const hookFn1 = vi.fn();
    const hookFn2 = vi.fn();

    onMountedOrActivated(hookFn1);
    onMountedOrActivated(hookFn2);

    expect(onMounted).toHaveBeenCalledTimes(2);
    expect(onActivated).toHaveBeenCalledTimes(2);
  });
});
