import { describe, it, expect, vi, beforeEach, afterEach } from 'vitest';
import { useTimeoutFn, useTimeoutRef } from '/@/hooks/core/useTimeout';

// Build configuration mocks
Object.defineProperty(globalThis, '__COLOR_PLUGIN_OUTPUT_FILE_NAME__', {
  value: 'mock-theme.css', writable: true
});

// Mock timers
vi.useFakeTimers();

describe('useTimeout coverage', () => {
  beforeEach(() => {
    vi.clearAllTimers();
  });

  afterEach(() => {
    vi.runOnlyPendingTimers();
    vi.useRealTimers();
    vi.useFakeTimers();
  });

  describe('useTimeoutRef', () => {
    it('should return readyRef, stop, and start functions', () => {
      const { readyRef, stop, start } = useTimeoutRef(1000);

      expect(readyRef).toBeDefined();
      expect(typeof stop).toBe('function');
      expect(typeof start).toBe('function');
      expect(readyRef.value).toBe(false);
    });

    it('should set readyRef to true after timeout', () => {
      const { readyRef } = useTimeoutRef(1000);

      expect(readyRef.value).toBe(false);

      vi.advanceTimersByTime(1000);

      expect(readyRef.value).toBe(true);
    });

    it('should stop timer when stop is called', () => {
      const { readyRef, stop } = useTimeoutRef(1000);

      stop();
      vi.advanceTimersByTime(1000);

      expect(readyRef.value).toBe(false);
    });

    it('should restart timer when start is called', () => {
      const { readyRef, start } = useTimeoutRef(1000);

      start();
      expect(readyRef.value).toBe(false);

      vi.advanceTimersByTime(1000);
      expect(readyRef.value).toBe(true);
    });
  });

  describe('useTimeoutFn', () => {
    it('should throw error if handle is not a function', () => {
      expect(() => {
        useTimeoutFn('not a function' as any, 1000);
      }).toThrow('handle is not Function!');
    });

    it('should execute handle after timeout', () => {
      const mockHandle = vi.fn();
      const { readyRef } = useTimeoutFn(mockHandle, 1000);

      expect(mockHandle).not.toHaveBeenCalled();
      expect(readyRef.value).toBe(false);

      vi.advanceTimersByTime(1000);

      expect(readyRef.value).toBe(true);
      // Note: watch might not trigger in test environment
    });

    it('should execute handle immediately when native is true', () => {
      const mockHandle = vi.fn();
      useTimeoutFn(mockHandle, 1000, true);

      expect(mockHandle).toHaveBeenCalled();
    });

    it('should return readyRef, stop, and start functions', () => {
      const mockHandle = vi.fn();
      const { readyRef, stop, start } = useTimeoutFn(mockHandle, 1000);

      expect(readyRef).toBeDefined();
      expect(typeof stop).toBe('function');
      expect(typeof start).toBe('function');
    });

    it('should stop timer when stop is called', () => {
      const mockHandle = vi.fn();
      const { readyRef, stop } = useTimeoutFn(mockHandle, 1000);

      stop();
      vi.advanceTimersByTime(1000);

      expect(mockHandle).not.toHaveBeenCalled();
      expect(readyRef.value).toBe(false);
    });

    it('should restart timer when start is called', () => {
      const mockHandle = vi.fn();
      const { readyRef, start } = useTimeoutFn(mockHandle, 1000);

      start();
      expect(readyRef.value).toBe(false);

      vi.advanceTimersByTime(1000);
      expect(readyRef.value).toBe(true);
      // Note: watch might not trigger in test environment
    }, 10000);
  });
});
