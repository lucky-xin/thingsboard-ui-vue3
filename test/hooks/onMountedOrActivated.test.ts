import { describe, it, expect, vi } from 'vitest';
import { nextTick, onMounted, onActivated } from 'vue';
import { mount } from '@vue/test-utils';
import { onMountedOrActivated } from '/@/hooks/core/onMountedOrActivated';

// Mock Vue lifecycle hooks
vi.mock('vue', async () => {
  const actual = await vi.importActual('vue');

// Build configuration mocks
Object.defineProperty(globalThis, '__COLOR_PLUGIN_OUTPUT_FILE_NAME__', {
  value: 'mock-theme.css', writable: true
});
  return {
    ...actual,
    nextTick: vi.fn(),
    onMounted: vi.fn(),
    onActivated: vi.fn(),
  };
});

describe('onMountedOrActivated', () => {
  it('should register hooks correctly', () => {
    const mockHook = vi.fn();

    onMountedOrActivated(mockHook);

    expect(onMounted).toHaveBeenCalled();
    expect(onActivated).toHaveBeenCalled();
  });

  it('should call hook on mounted', () => {
    const mockHook = vi.fn();
    const mockOnMounted = vi.fn((callback) => {
      callback(); // Simulate onMounted being called
    });

    (onMounted as any).mockImplementation(mockOnMounted);
    (nextTick as any).mockImplementation((callback) => {
      callback(); // Simulate nextTick completion
    });

    onMountedOrActivated(mockHook);

    expect(mockHook).toHaveBeenCalledTimes(1);
  });

  it('should call hook on activated if mounted', () => {
    const mockHook = vi.fn();
    let mountedCallback: () => void;
    let activatedCallback: () => void;

    const mockOnMounted = vi.fn((callback) => {
      mountedCallback = callback;
    });
    const mockOnActivated = vi.fn((callback) => {
      activatedCallback = callback;
    });

    (onMounted as any).mockImplementation(mockOnMounted);
    (onActivated as any).mockImplementation(mockOnActivated);
    (nextTick as any).mockImplementation((callback) => {
      callback(); // Simulate nextTick completion
    });

    onMountedOrActivated(mockHook);

    // Simulate mounted
    mountedCallback!();

    // Simulate activated
    activatedCallback!();

    expect(mockHook).toHaveBeenCalledTimes(2);
  });

  it('should not call hook on activated if not mounted', () => {
    const mockHook = vi.fn();
    let activatedCallback: () => void;

    const mockOnActivated = vi.fn((callback) => {
      activatedCallback = callback;
    });

    (onMounted as any).mockImplementation(vi.fn());
    (onActivated as any).mockImplementation(mockOnActivated);
    (nextTick as any).mockImplementation(vi.fn());

    onMountedOrActivated(mockHook);

    // Simulate activated without mounted
    activatedCallback!();

    expect(mockHook).toHaveBeenCalledTimes(0);
  });

  it('should handle multiple calls correctly', () => {
    const mockHook1 = vi.fn();
    const mockHook2 = vi.fn();
    let mountedCallback1: () => void;
    let mountedCallback2: () => void;

    const mockOnMounted = vi.fn((callback) => {
      if (!mountedCallback1) {
        mountedCallback1 = callback;
      } else {
        mountedCallback2 = callback;
      }
    });

    (onMounted as any).mockImplementation(mockOnMounted);
    (nextTick as any).mockImplementation((callback) => {
      callback();
    });

    onMountedOrActivated(mockHook1);
    onMountedOrActivated(mockHook2);

    mountedCallback1!();
    mountedCallback2!();

    expect(mockHook1).toHaveBeenCalledTimes(1);
    expect(mockHook2).toHaveBeenCalledTimes(1);
  });
});
