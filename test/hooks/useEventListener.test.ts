import { describe, it, expect, vi } from 'vitest';
import { ref, nextTick } from 'vue';
import { useEventListener } from '/@/hooks/event/useEventListener';

// Build configuration mocks
Object.defineProperty(globalThis, '__COLOR_PLUGIN_OUTPUT_FILE_NAME__', {
  value: 'mock-theme.css', writable: true
});

describe('hooks/event/useEventListener', () => {
  it('should add and remove event listener with debounce (default)', async () => {
    const target = document.createElement('div');
    const listener = vi.fn();
    const { removeEvent } = useEventListener({ el: target, name: 'click', listener, wait: 0 });

    target.dispatchEvent(new Event('click'));
    await nextTick();
    expect(listener).toHaveBeenCalledTimes(1);

    removeEvent();
    target.dispatchEvent(new Event('click'));
    await nextTick();
    expect(listener).toHaveBeenCalledTimes(1);
  });

  it('should support throttle mode', async () => {
    const target = document.createElement('div');
    const listener = vi.fn();
    useEventListener({ el: target, name: 'scroll', listener, isDebounce: false, wait: 0 });
    target.dispatchEvent(new Event('scroll'));
    await nextTick();
    expect(listener).toHaveBeenCalledTimes(1);
  });

  it('should autoRemove on cleanup', async () => {
    const target = ref<HTMLElement>();
    const listener = vi.fn();
    useEventListener({ el: target, name: 'click', listener, wait: 0, autoRemove: true });
    target.value = document.createElement('button');
    await nextTick();
    target.value!.dispatchEvent(new Event('click'));
    await nextTick();
    expect(listener).toHaveBeenCalledTimes(1);
  });
});
