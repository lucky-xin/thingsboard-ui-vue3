import { describe, it, expect, vi, beforeEach } from 'vitest';
import { useAppInject } from '/@/hooks/web/useAppInject';

// Mock the Application component context
vi.mock('/@/components/Application', () => ({
  useAppProviderContext: vi.fn(),
}));

// Mock Vue composition functions
vi.mock('vue', () => ({
  computed: vi.fn((fn) => ({ value: fn() })),
  unref: vi.fn((val) => (val && typeof val === 'object' && 'value' in val ? val.value : val)),
}));

import { useAppProviderContext } from '/@/components/Application';
import { computed, unref } from 'vue';

// Build configuration mocks
Object.defineProperty(globalThis, '__COLOR_PLUGIN_OUTPUT_FILE_NAME__', {
  value: 'mock-theme.css', writable: true
});

describe('hooks/web/useAppInject', () => {
  beforeEach(() => {
    vi.clearAllMocks();
  });

  it('should return getIsMobile computed property', () => {
    const mockValues = {
      isMobile: { value: true },
    };

    (useAppProviderContext as any).mockReturnValue(mockValues);
    (computed as any).mockImplementation((fn) => ({ value: fn() }));
    (unref as any).mockImplementation((val) => val.value);

    const result = useAppInject();

    expect(result).toHaveProperty('getIsMobile');
    expect(useAppProviderContext).toHaveBeenCalledTimes(1);
    expect(computed).toHaveBeenCalledTimes(1);
  });

  it('should correctly handle mobile state through computed getter', () => {
    const mockValues = {
      isMobile: { value: false },
    };

    (useAppProviderContext as any).mockReturnValue(mockValues);
    (computed as any).mockImplementation((fn) => ({ value: fn() }));
    (unref as any).mockImplementation((val) => val.value);

    const result = useAppInject();

    // Test the computed function was called with correct callback
    const computedCallback = (computed as any).mock.calls[0][0];
    expect(typeof computedCallback).toBe('function');

    // Test unref is called within the computed callback
    computedCallback();
    expect(unref).toHaveBeenCalledWith(mockValues.isMobile);
  });

  it('should handle different mobile state values', () => {
    const testCases = [true, false, undefined, null];

    testCases.forEach((mobileState) => {
      vi.clearAllMocks();

      const mockValues = {
        isMobile: { value: mobileState },
      };

      (useAppProviderContext as any).mockReturnValue(mockValues);
      (computed as any).mockImplementation((fn) => ({ value: fn() }));
      (unref as any).mockImplementation((val) => val.value);

      const result = useAppInject();
      const computedCallback = (computed as any).mock.calls[0][0];

      computedCallback();
      expect(unref).toHaveBeenCalledWith(mockValues.isMobile);
    });
  });

  it('should create computed property that calls unref correctly', () => {
    const mockIsMobile = { value: true };
    const mockValues = { isMobile: mockIsMobile };

    (useAppProviderContext as any).mockReturnValue(mockValues);
    (computed as any).mockImplementation((fn) => ({ value: fn() }));
    (unref as any).mockReturnValue(true);

    useAppInject();

    const computedFn = (computed as any).mock.calls[0][0];
    const result = computedFn();

    expect(unref).toHaveBeenCalledWith(mockIsMobile);
  });

  it('should handle context provider errors gracefully', () => {
    (useAppProviderContext as any).mockImplementation(() => {
      throw new Error('Context not found');
    });

    expect(() => useAppInject()).toThrow('Context not found');
  });
});
