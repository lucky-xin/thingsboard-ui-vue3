import { describe, it, expect, vi, beforeEach } from 'vitest';
import { onUnmounted, getCurrentInstance } from 'vue';
import { createContextMenu, destroyContextMenu } from '/@/components/ContextMenu';
import { useContextMenu } from '/@/hooks/web/useContextMenu';

// Build configuration mocks
Object.defineProperty(globalThis, '__COLOR_PLUGIN_OUTPUT_FILE_NAME__', {
  value: 'mock-theme.css', writable: true
});

// Mock Vue composition API
vi.mock('vue', () => ({
  onUnmounted: vi.fn(),
  getCurrentInstance: vi.fn(),
}));

// Mock ContextMenu components
vi.mock('/@/components/ContextMenu', () => ({
  createContextMenu: vi.fn(),
  destroyContextMenu: vi.fn(),
}));

describe('hooks/web/useContextMenu', () => {
  beforeEach(() => {
    vi.clearAllMocks();
  });

  it('should return context menu functions', () => {
    vi.mocked(getCurrentInstance).mockReturnValue(null);

    const result = useContextMenu();

    expect(result).toEqual([createContextMenu, destroyContextMenu]);
  });

  it('should register unmount callback when current instance exists and authRemove is true', () => {
    const mockInstance = { test: 'instance' };
    vi.mocked(getCurrentInstance).mockReturnValue(mockInstance);

    useContextMenu(true);

    expect(onUnmounted).toHaveBeenCalledWith(expect.any(Function));
  });

  it('should not register unmount callback when authRemove is false', () => {
    const mockInstance = { test: 'instance' };
    vi.mocked(getCurrentInstance).mockReturnValue(mockInstance);

    useContextMenu(false);

    expect(onUnmounted).not.toHaveBeenCalled();
  });

  it('should call destroyContextMenu on unmount when authRemove is true', () => {
    const mockInstance = { test: 'instance' };
    vi.mocked(getCurrentInstance).mockReturnValue(mockInstance);

    useContextMenu(true);

    // Get the callback passed to onUnmounted
    const unmountCallback = vi.mocked(onUnmounted).mock.calls[0][0];
    unmountCallback();

    expect(destroyContextMenu).toHaveBeenCalled();
  });

  it('should not register unmount callback when no current instance', () => {
    vi.mocked(getCurrentInstance).mockReturnValue(null);

    useContextMenu(true);

    expect(onUnmounted).not.toHaveBeenCalled();
  });

  it('should default authRemove to true', () => {
    const mockInstance = { test: 'instance' };
    vi.mocked(getCurrentInstance).mockReturnValue(mockInstance);

    useContextMenu();

    expect(onUnmounted).toHaveBeenCalledWith(expect.any(Function));
  });
});
