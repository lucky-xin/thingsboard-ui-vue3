describe('store/modules/permission extra', () => {
  it('should build routes with ROLE mode and set affix home safely', async () => {
    setActivePinia(createPinia());
    const store = usePermissionStore();
    const routes = await store.buildRoutesAction();
    expect(Array.isArray(routes)).toBe(true);
    expect(routes.find((r: any) => r?.path === '/error-log')).toBeTruthy();
  });

  it('should handle BACK mode without throwing', async () => {
    vi.doMock('/@/store/modules/app', () => ({
      useAppStoreWithOut: () => ({ getProjectConfig: { permissionMode: PermissionModeEnum.BACK } }),
    }));
    setActivePinia(createPinia());
    const { usePermissionStore: usePermissionStore2 } = await import('/@/store/modules/permission');
    const store2 = usePermissionStore2();
    const routes2 = await store2.buildRoutesAction();
    expect(routes2.find((r: any) => r?.path === '/error-log')).toBeTruthy();
  });
});
